#Faça um Programa que calcule a área de um quadrado, em seguida 
#mostre o dobro desta área para o usuário.

base = float (input('Informe o comprimento da base do quadrado: '))
alt = float (input('Informe o altura do quadrado: '))
area = (base * alt) * 2
print(f'O dobro da área do quadrado é {area}m².')